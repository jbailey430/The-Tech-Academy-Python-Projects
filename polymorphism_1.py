class Dog():
    def run(self):
        print("The dog is running.")
    def look_up(self):
        print("Dogs cant look up.")
    def skeleton(self):
        print("A dogs body is made up of bones.")

class Cat():
    def run(self):
        print("The Cat is running.")
    def look_up(self):
        print("Cats can look up.")
    def skeleton(self):
        print("A cat's skeleton is made up of bones.")

charlie = Dog()
charlie.look_up()

sam = Cat()
sam.skeleton()
